#define mat vector<vector<int>>
int p = 1e9+7;

mat mul(mat a, mat b) {
    mat c(a.size(), vector<int>(b[0].size()));
    for (int i = 0; i < a.size(); i++) {
        for (int j = 0; j < b[0].size(); j++) {
            for (int k = 0; k < b.size(); k++) {
                c[i][j] = (c[i][j] + a[i][k]*b[k][j]%p)%p;
            }
        }
    }
    return c;
}

